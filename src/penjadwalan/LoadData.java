    public void tampil_tb_matakuliah(){
        int i,nDataMK;
        String sql;
        ArrayList<MataKuliah> mataKuliahs;
        ArrayList<Dosen> dosens;
        
        DatabaseManager databaseManager = new DatabaseManager();
        databaseManager.setKoneksiDatabase();
        
        sql = "select count(*) from mata_kuliah";
        nDataMK = databaseManager.getJumlahDataMK(sql);
        
        sql = "select *from mata_kuliah";
        mataKuliahs = databaseManager.getDataMataKuliah(sql);
        
        String[] row = new String[7];

        DefaultTableModel mataKuliahTable = (DefaultTableModel) tblMK.getModel();
        for(i=tblMK.getRowCount()-1; i>=0; i--){
            mataKuliahTable.removeRow(i);
        }
        
        for(i=0; i<nDataMK; i++){
            row[0] = String.valueOf(mataKuliahs.get(i).getIdMK());
            row[1] = mataKuliahs.get(i).getKelas();
            row[2] = mataKuliahs.get(i).getNamaMK();
            row[3] = mataKuliahs.get(i).getDosenPengampuMK();
            row[4] = String.valueOf(mataKuliahs.get(i).getSksMK());
            row[5] = mataKuliahs.get(i).getSemesterMK();
            row[6] = mataKuliahs.get(i).getKodeMK();
            
            mataKuliahTable.addRow(row);
        }
    }
    
