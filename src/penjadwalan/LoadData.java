    private void BtnSimpanDataMKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnSimpanDataMKActionPerformed
        // TODO add your handling code here:
        int nDataMK;
        String sql;
        MataKuliah MK;
        
        String mataKuliah = TFMK.getText();
        int sks = Integer.parseInt(CBSksMK.getSelectedItem().toString());
        String dosenPengampu = CBDosenPengampuMK.getSelectedItem().toString();
        String semesterMK = CBSemesterMK.getSelectedItem().toString();
        String semester = CBSemester.getSelectedItem().toString();
        String kode = TFKodeMK.getText();
        String kelas = CBKelas.getSelectedItem().toString();
        
        DatabaseManager databaseManager = new DatabaseManager();
        databaseManager.setKoneksiDatabase();
        
        sql = "select max(id_mk) from mata_kuliah";
        nDataMK = databaseManager.getJumlahDataMK(sql);
        
        nDataMK = nDataMK + 1;
        MK = new MataKuliah();
        MK.setIdMK(nDataMK);
        MK.setKelas(kelas);
        MK.setNamaMK(mataKuliah);
        MK.setDosenPengampuMK(dosenPengampu);
        MK.setSksMK(sks);
        MK.setSemesterMK(semesterMK);
        MK.setKodeMK(kode);
        MK.setSemester(semester);
        
        sql = "INSERT INTO mata_kuliah (id_mk, kelas, nama_mk, dosen_pengampu, sks_mk, semester_mk, kode_mk, s_semester) VALUES (?, ?, ?, ?, ?, ?, ?, ?)";
        databaseManager.insertDataMK(sql, MK); 
        tampil_tb_matakuliah();
    }//GEN-LAST:event_BtnSimpanDataMKActionPerformed

